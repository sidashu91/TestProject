package basicPrograms;

public class Array_PyramidPattern {

	public static void main(String[] args) {
		
		
		int n=5;
	
		for (int i = 0; i < n; i++) {
			
			System.out.println(" ");
			
			
			for (int j = 0; j <i; j++) {
				
				System.out.println("* ");
				
			}
			System.out.println();
			
		}
		

	}

}
