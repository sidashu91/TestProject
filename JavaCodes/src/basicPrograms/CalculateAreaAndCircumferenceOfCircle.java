public class CalculateAreaAndCircumferenceOfCircle {

	
	public static void main(String[] args) {
		
		float pie= 3.14f;
		float   r=2.3f ;
		float area= pie * r*r;		
		float circumference= 2*pie*r;
		
		System.out.println("Are of the Circle =" +area+"\n"+ "Circumference of the Circle ="+ circumference);
		
		
		
	}
}
